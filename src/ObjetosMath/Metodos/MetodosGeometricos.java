package ObjetosMath.Metodos;

public interface MetodosGeometricos {
    /**
     * @apiNote calcularArea()
     * Se encarga del cálculo de áreas de acuerdo con el objeto que lo haya heredado
     * @apiNote calcularPerimetro()
     * Se encarga de calcular el perímetor de acuerdo con el objeto que lo haya heredado
     * @apiNote dibujar()
     * Utilizado para dibujar las figuras geométricas
     * */
    double calcularArea();
    double calcularPerimetro();
    void dibujar();
}
